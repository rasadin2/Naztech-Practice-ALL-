
public class Calculator {
	
	
	
	public int add(int x, int y) {
		
		int z =x+y;
		//System.out.println("Integer Result="+z);
		return z;
	}
	
	public int add(int x,int y,int z) {
		
		int result=x+y+z;
		return result;
		
	}
	
	public double add(double x,double y) {
		
		double result=x+y;
		return result;
		
	}
	
	

}
